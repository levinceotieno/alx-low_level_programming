/**
 * op_add - addition of INTEGER
 * @a: arguement1
 * @b: arguement2
 * Return: summation
 */

int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - subb INTEGER
 * @a: arguement1
 * @b: arguement2
 * Return: the diff
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multi.. INTEGER
 * @a: arguement1
 * @b: arguement2
 * Return: product
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - div INTEGER
 * @a: arguement1
 * @b: arguement2
 * Return: div
 */

int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - mod of INTEGER
 * @a: arguement1
 * @b: arguement2
 * Return: rem...
 */

int op_mod(int a, int b)
{
return (a % b);
}

